// Mock IntersectionObserver
class MockIntersectionObserver {
  constructor(callback) {
    this.callback = callback;
  }

  observe() {
    this.callback([{ isIntersecting: true }]);
  }

  unobserve() {}
  disconnect() {}
}

window.IntersectionObserver = MockIntersectionObserver;

